int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	while ((i * i) < nb)
		i++;
	if ((nb % i) == 0)
		return (i);
	else
		return (0);
}
